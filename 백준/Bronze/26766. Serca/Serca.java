import java.util.Scanner;

public class Main {
public static void main(String[] args) {
	
	Scanner scan = new Scanner(System.in);
	int a = scan.nextInt();
	
	while(a!=0) {
		
		System.out.println(" @@@   @@@ ");
		System.out.println("@   @ @   @");
		System.out.println("@    @    @");
		System.out.println("@         @");
		System.out.println(" @       @ ");
		System.out.println("  @     @  ");
		System.out.println("   @   @   ");
		System.out.println("    @ @    ");
		System.out.println("     @     ");
		a--;
	}
}
}